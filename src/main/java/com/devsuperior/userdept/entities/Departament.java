package com.devsuperior.userdept.entities;

public class Departament {

	private Long id;
	private String name;
	
	public Departament() {
		
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
}
